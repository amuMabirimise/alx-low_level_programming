int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);
/**
 * add - addition
 * @a: first number
 * @b: second number
 * Return: sucess
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub- subtraction
 * @a: first number
 * @b: second number
 * Return: sucess
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul -Multiplication
 * @a: first number
 * @b: second number
 * Return: sucess
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division
 * @a: first number
 * @b: second number
 * Return: sucess
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus
 * @a: first number
 * @b: second number
 * Return: sucess
 */
int mod(int a, int b)
{
	return (a % b);
}
